package com.tianque.plugin.account.vo;

import java.io.Serializable;

public class LedgerPoorPeopleDetail extends LedgerDetail implements Serializable {
	private Integer poorType;//困难类型
	private Integer poorSource;//困难原因
	private Integer requiredType;//具体需求
	public Integer getPoorType() {
		return poorType;
	}
	public void setPoorType(Integer poorType) {
		this.poorType = poorType;
	}
	public Integer getPoorSource() {
		return poorSource;
	}
	public void setPoorSource(Integer poorSource) {
		this.poorSource = poorSource;
	}
	public Integer getRequiredType() {
		return requiredType;
	}
	public void setRequiredType(Integer requiredType) {
		this.requiredType = requiredType;
	}
}
