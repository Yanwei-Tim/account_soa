package com.tianque.plugin.account.domain;

import java.io.Serializable;

import com.tianque.core.base.BaseDomain;
import com.tianque.domain.Organization;

public class ThreeRecordsYearTurn extends BaseDomain implements Serializable {
	private int year;
	private Organization turnOrg;
	private int ledgerType;
	private Long oldLedgerId;
	private Long newLedgerId;
	private Organization createOrg;
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public Organization getTurnOrg() {
		return turnOrg;
	}
	public void setTurnOrg(Organization turnOrg) {
		this.turnOrg = turnOrg;
	}
	public int getLedgerType() {
		return ledgerType;
	}
	public void setLedgerType(int ledgerType) {
		this.ledgerType = ledgerType;
	}
	public Long getOldLedgerId() {
		return oldLedgerId;
	}
	public void setOldLedgerId(Long oldLedgerId) {
		this.oldLedgerId = oldLedgerId;
	}
	public Long getNewLedgerId() {
		return newLedgerId;
	}
	public void setNewLedgerId(Long newLedgerId) {
		this.newLedgerId = newLedgerId;
	}
	public Organization getCreateOrg() {
		return createOrg;
	}
	public void setCreateOrg(Organization createOrg) {
		this.createOrg = createOrg;
	}
}
